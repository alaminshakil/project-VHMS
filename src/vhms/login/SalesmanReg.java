    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        String name = txtName.getText();
        String email = txtEmail.getText();
        String address = txtAddress.getText();
        String age = txtAge.getText();
        char p[] = txtPassword.getPassword();
        String password = String.valueOf(p);

        Connection con = null;
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/lunch_box", "root", "123");
            Statement stmt = con.createStatement();
            int count = 0;
            count = stmt.executeUpdate("insert into sales_man(name,email,address, age, password) values('" + name + "','" + email + "','" + address +"','"+age+"','"+password+"')");
            if (count > 0) {
                JOptionPane.showMessageDialog(this, "Registration successfull, you can Add Another now");
               clear();

            } else {
                JOptionPane.showMessageDialog(this, "Data not inserted", "error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            System.out.println(e);
        } finally {
            try {
                con.close();

            } catch (SQLException e) {
                System.out.println(e);
            }
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnSubmitActionPerformed

