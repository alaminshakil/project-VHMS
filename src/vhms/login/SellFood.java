    public void table() {

        if (inv_table.getRowCount() > 0) {
            for (int i = inv_table.getRowCount() - 1; i > -1; i--) {
                model.removeRow(i);
            }
        }
        try {
            con = Database.getConnection();
            String sql = "select item_name,unit_price,sell_unit, price from temp_cart";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("item_name"), rs.getString("unit_price"), rs.getString("sell_unit"), rs.getString("price")});
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

    private void reset() {
        try {
            con = Database.getConnection();
            Statement statement = con.createStatement();
            statement.executeUpdate("TRUNCATE temp_cart");
            JOptionPane.showMessageDialog(this, "Cart is empty now");

        } catch (SQLException e) {
            System.out.println("Could not truncate test_table " + e.getMessage());

        }
        invoiceNo.setText("");
        customerName.setText("");
        customerPhone.setText("");
        payable.setText("");
        vat.setText("");
        paidAmount.setText("");
        returnedAmount.setText("");
        table();
    }

    private void getDate() {
        LocalDate localDate = LocalDate.now();//For reference
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd-MM-yyyy");
        String dateToday = localDate.format(formatter);
        invoiceDate.setText(dateToday);
    }

    private void getTotal() {
        try {
            con = Database.getConnection();
            String query = "select sum(price) from temp_cart";
            PreparedStatement pst = con.prepareStatement(query);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                String totalprice = rs.getString("sum(price)");
                payable.setText(totalprice);
                vat.setText(String.valueOf(Integer.valueOf(totalprice) * .04));
            }
        } catch (SQLException e) {
        }

    }

