    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        String sql = "INSERT INTO temp_cart (item_name,unit_price,sell_unit,price,item_profit) VALUES (?,?,?,?,?)";
        try {
            con = Database.getConnection();
            int profit = ((Integer.parseInt(sellPrice.getText()) * Integer.parseInt(sellUnit.getText())) - (Integer.parseInt(purchasePrice.getText()) * Integer.parseInt(sellUnit.getText())));
            String itemProfit = String.valueOf(profit);

            pst = con.prepareStatement(sql);
            pst.setString(1, itemName.getText());
            pst.setString(2, sellPrice.getText());
            pst.setString(3, sellUnit.getText());
            pst.setString(4, itemTotal.getText());
            pst.setString(5, itemProfit);

            pst.execute();

            JOptionPane.showMessageDialog(null, "Sucessfully Added to Cart...!!");
            table();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        getTotal();
        sellUnit.setText("");
        itemTotal.setText("");
        purchasePrice.setText("");


    }//GEN-LAST:event_btnAddActionPerformed

